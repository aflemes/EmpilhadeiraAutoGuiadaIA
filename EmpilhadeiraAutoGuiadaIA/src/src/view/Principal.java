    public void initNodos(){
        JPanel labirinto[][] = new JPanel[12][12];
        int coordenadaX = 0, coordenadaY = 0;
        coordenadaY = 60;
        coordenadaX = 50;
        
        for (int y = 0; y < 10; y++) {
            JPanel nodo = new JPanel();
            nodo.setSize(50, 40);
            nodo.setOpaque(false);
//            nodo.setBackground(Color.white);
            nodo.setLocation(coordenadaX, coordenadaY);
            nodo.setVisible(true);
            nodo.setLayout(null);
            
            this.add(nodo);
            
            for (int x = 0; x < 10; x++) {
                labirinto[x][y] = nodo;
                
                coordenadaX += 50;
                
                nodo = new JPanel();
                nodo.setSize(50, 40);
                nodo.setOpaque(false);
//                nodo.setBackground(Color.white);
                nodo.setLocation(coordenadaX, coordenadaY);
                nodo.setLayout(null);
                if (!mapa[y][x].equals(" ")){
                    nodo = initParedes(mapa[y][x],nodo);
                }
                
                this.add(nodo);
            }
            coordenadaY += 40;
            coordenadaX = 50;
        }
    }
    
    public JPanel initParedes(String value,JPanel nodo){
        JPanel parede; 
        int counter = 0;
        String newValue;
        Color corParede = Color.blue;
        
        while(true){
            try{
                newValue = value.substring(counter, counter + 1);
                counter++;
            }
            catch(Exception e){
                break;
            }
            
            parede = new JPanel();
            parede.setVisible(true);
            
            switch(newValue){
                case "N":
                    parede.setBackground(corParede);
                    parede.setSize(50,4);
                    parede.setLocation(0, 0);
                    break;
                case "O":
                    parede.setBackground(corParede);
                    parede.setSize(4,40);
                    parede.setLocation(0, 0);
                    break;
                case "L":
                    parede.setBackground(corParede);
                    parede.setSize(4,40);
                    parede.setLocation(46, 0);
                    break;
                case "S":
                    parede.setBackground(corParede);
                    parede.setSize(50,4);
                    parede.setLocation(0, 36);
                    break;
            }
            nodo.add(parede);
        }
        
        return nodo;
    }


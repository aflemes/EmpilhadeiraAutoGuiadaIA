    public void initLabirinto(){
        JPanel labirinto[][] = new JPanel[10][10];
        int coordenadaX = 0, coordenadaY = 0;
        
        coordenadaY = 100;
        coordenadaX = 50;
        
        for (int y = 0; y < 10; y++) {
            JPanel nodo = new JPanel();
            nodo.setSize(50, 40);
            nodo.setBackground(Color.black);
            nodo.setLocation(coordenadaY, coordenadaX);
            nodo.setVisible(true);
            
            this.add(nodo);
            
            for (int x = 0; x < 10; x++) {
                labirinto[x][y] = nodo;
                
                coordenadaX += 41;
                
                nodo = new JPanel();
                nodo.setSize(50, 40);
                nodo.setBackground(Color.black);
                nodo.setLocation(coordenadaY, coordenadaX);
                this.add(nodo);
            }
            coordenadaY += 51;
            coordenadaX = 50;
        }
    }

